function esc(value) {
  if (value === undefined || value === null || value === '') return '-';
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function formatTimeStamp() {
  return new Date().toLocaleString('en-CA', { timeZone: 'America/Toronto' });
}

function sectionTitle(text) {
  return `<h2 style="font-size:14px;margin:20px 0 10px;color:#0f2b46;border-bottom:1px solid #dbe2ea;padding-bottom:6px;text-transform:uppercase;letter-spacing:.08em;">${esc(text)}</h2>`;
}

function pairGrid(pairs = []) {
  const cells = pairs
    .map(
      ({ label, value }) => `
      <div style="padding:10px;border:1px solid #dbe2ea;border-radius:6px;background:#fff;">
        <div style="font-size:11px;color:#64748b;text-transform:uppercase;letter-spacing:.08em;margin-bottom:4px;">${esc(label)}</div>
        <div style="font-size:14px;color:#0f172a;font-weight:600;">${esc(value)}</div>
      </div>
    `,
    )
    .join('');

  return `<div style="display:grid;grid-template-columns:1fr 1fr;gap:10px;">${cells}</div>`;
}

function textareaBlock(label, value) {
  return `
    <div style="margin-top:10px;">
      <div style="font-size:11px;color:#64748b;text-transform:uppercase;letter-spacing:.08em;margin-bottom:4px;">${esc(label)}</div>
      <div style="min-height:70px;border:1px solid #dbe2ea;border-radius:6px;padding:10px;white-space:pre-wrap;line-height:1.45;">${esc(value)}</div>
    </div>
  `;
}

