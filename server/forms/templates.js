export function buildEmailHTML({ formType, formName, formData, profile, validation }) {
  const validationLine = validation?.canExport
    ? '<span style="color:#16a34a;font-weight:700;">Validation: PASS</span>'
    : '<span style="color:#dc2626;font-weight:700;">Validation: Review Required</span>';

  const top = `
    <p style="margin:0 0 10px;">Hello Team,</p>
    <p style="margin:0 0 10px;">A ${esc(formName)} has been prepared in MedicOS and is ready for review.</p>
    <p style="margin:0 0 14px;">${validationLine}</p>
    <p style="margin:0 0 14px;">
      Submitted by: <strong>${esc(`${profile.firstName || ''} ${profile.lastName || ''}`.trim())}</strong><br />
      Medic Number: <strong>${esc(profile.medicNumber)}</strong>
    </p>
  `;

  const rows = Object.entries(formData || {})
    .map(
      ([key, value]) =>
        `<tr><td style="border:1px solid #e2e8f0;padding:6px 8px;background:#f8fafc;font-size:12px;">${esc(key)}</td><td style="border:1px solid #e2e8f0;padding:6px 8px;font-size:12px;">${esc(value)}</td></tr>`,
    )
    .join('');

  return `
  <div style="font-family:Arial,sans-serif;color:#0f172a;line-height:1.45;">
    ${top}
    <table style="width:100%;border-collapse:collapse;margin-top:8px;">${rows}</table>
    <p style="margin-top:14px;">Regards,<br />MedicOS AI Assistant</p>
  </div>`;
}

export function applyTemplateValues(template, values = {}) {
  let out = template || '';
  for (const [key, value] of Object.entries(values)) {
    out = out.replaceAll(`{${key}}`, value ?? '');
  }
  return out;
}
